<p>Welcome! You've stumbled onto <u>this is our thing.</u>, a shrine dedicated to the relationship between Mark Zuckerberg/Eduardo Saverin from the <i>Social Network</i> (and not to be confused with the relationship between their real life counterparts.) This is <a href="http://arose.nu">my</a> first shrine, so you may easily presume that the relationship means very much to me <strike>and that I apologize for early bugs and troubleshooting</strike>.</p>

<p><i>The Social Network</i>, commonly referred to as "the Facebook movie," was released on 4 October 2010, directed by David Fincher and written by Aaron Sorkin. It is, by all technical means, the movie adaptation of <i>The Accidental Billionaires</i> by Ben Mezrich, which was earlier published 14 July 2009. It can be correctly assumed that Mark Zuckerberg is the protagonist of the movie, being the original founder of Facebook and its (real life) current CEO. However, the narrative of the movie is framed in such a way that his best friend, Eduardo Saverin, is narrating and showing much of the main storyline from his point of view; so Eduardo can be considered the protagonist as well. Either way, it is a dual role between the two.</p>

<p>The movie&mdash;and Mark and Eduardo's relationship within the movie&mdash;can, in my opinion, be examined much more beyond the surface. Though the relationship depicted in the movie is self-stated as a "best friendship," there is quite a bit of homoerotic subtext without looking too closely. While constituents of this website will acknowledge the relationship as presented in the movie canon, the articles will also be written intending such subtext. Additionally, elements of this website are personal interpretations theorized by yours truly, including but not limited to personality types, hypothetical Hogwarts Housing, and intentions of characters' actions not explicitly stated in the movie.</p>

<p>If you continue journeying through this website, there absolutely will be <b>unmarked spoilers ahead</b>, so please be sure to proceed with caution if you have not yet seen the movie. It is only 120 minutes of your time, though, if you are interested.</p>

